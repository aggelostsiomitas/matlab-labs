function [c] = orderstring(s1,s2)
%για την εργασια υποθεσαμε εχουν το ιδιο μηκος 

for i=1:length(s1)
    if s1(i)>s2(i)
        c=0;
        break;
    else 
        c=1;
    end
end  
% ή για να αποφυγω ενα else να βαλω το c=0 ή 1 απο την αρχη και με το
%if να τα αλλαξω δηλαδη να βαλω απο την αρχη c=1(s1<s2)
% και μετα if (s1(i)>s2(i)) να κανω το c μηδεν(c=0)
end

