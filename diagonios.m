%απο την 3η στηλη ως την 5η
b(2:n,1:n-1)=b(2:n,1:n-1)-eye(n-1);%n-1 γιατι εχω τωρα 4*4
b(3:n,1:n-2)=b(3:n,1:n-2)-eye(n-2);%n-2 γιατι εχω τωρα 3*3
