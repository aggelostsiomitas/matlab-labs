a(2:n,1:n-1)=a(2:n,1:n-1)-eye(n-1);
disp(a);
v=[2 2 2 2 2 ];
disp(v');

for i=1:n
    for j=max(i-D+1,1):min(i+D-1,n)
        s=a.*v;
    end
end
disp(s);
